Tipos de varíaveis

inteiro: Tipo inteiro = 1,2,3 etc..

float: 1,2/1,3/ etc...

string ou caracteres: cadeia de caracteres pode ser aspas simples ''ou aspas duplas "" 
  exemplos de string "1", "Anderson", 'string' tudo dentro das aspas são string, "1", 'a'->
   são caracter, passou de uma palavra ou letra são string

   não podemos somar números com caracter ex:
   1 +'1' (rodar no cmd --> irb para teste)
   
booleano: true ou false (verdadeiro ou falso)
